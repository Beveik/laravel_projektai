     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $book = new Books; // tuščias

        $book->title = $request->book_title; // tas pats, kas $_GET
        $book->book_code = $request->book_code;
        $book->pages = $request->book_pages;
        $book->description = $request->book_description; // tas pats, kas $_GET
        $book->author_id = $request->author_id;

        $book->save(); // insert į duomenų bazę

        return redirect()->route('books.index'); // automatinis nukėlimas į index
    }

